def characteristics_of_heroes(hero_name):
    if hero_name in heroes:
        hero_stats = heroes[hero_name]
        primary_attribute = hero_stats["Primary Attribute"]
        roles =",\t".join(hero_stats["Role"])

        print(f"Характеристики героя {hero_name}:")
        print(f"Primary Attribute:{primary_attribute}")
        print(f"Роли:{roles}")

def choise_hero():
    for hero in heroes:
        print(hero)
    while(True):
        choisehero=str(input("Выбор героя : "))
        if (choisehero in heroes):
            characteristics_of_heroes(choisehero)
            break
        else:
            print("нет такого")

